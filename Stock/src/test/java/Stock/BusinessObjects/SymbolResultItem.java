package stock.BusinessObjects;

public class SymbolResultItem {
	private String symbol;

	  public String getSymbol() { return this.symbol; }

	  public void setSymbol(String symbol) { this.symbol = symbol; }

	  private String name;

	  public String getName() { return this.name; }

	  public void setName(String name) { this.name = name; }

	  private String exch;

	  public String getExch() { return this.exch; }

	  public void setExch(String exch) { this.exch = exch; }

	  private String type;

	  public String getType() { return this.type; }

	  public void setType(String type) { this.type = type; }

	  private String exchDisp;

	  public String getExchDisp() { return this.exchDisp; }

	  public void setExchDisp(String exchDisp) { this.exchDisp = exchDisp; }

	  private String typeDisp;

	  public String getTypeDisp() { return this.typeDisp; }

	  public void setTypeDisp(String typeDisp) { this.typeDisp = typeDisp; }
}
